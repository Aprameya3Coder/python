# Kiran wants to store the number of apples, oranges, and bananas
# she has in the variables. Help her write a Python program that takes the number of
# apples, oranges, and bananas as input and stores them in variables. Print these
# variables.

apples = int(input("Enter the number of apples: "))
bananas = int(input("Enter the number of bananas"))
oranges = int(input("Enter the number of oranges"))
print("apples", apples)
print("bananas", bananas)
print("oranges", oranges)